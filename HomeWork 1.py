# Задание 1. Вычислить и вывести на экран длину окружности и площадь круга одного и того же заданного радиуса R, который необходимо ввести с клавиатуры в сантиметрах. 
# Результаты должны округляться до сотых.
import math

print("Введите значение R: ")
R = int(input())
C = round(2 * math.pi * R, 2)
S = round(math.pi * R ** 2, 2)
print(S)
print(C)

